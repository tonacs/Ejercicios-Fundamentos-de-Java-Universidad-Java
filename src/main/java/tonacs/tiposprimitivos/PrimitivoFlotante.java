package tonacs.tiposprimitivos;


public class PrimitivoFlotante {
    public static void main(String[] args) {
            float floatVar=1000.10F;
            System.out.println("floatVar = " + floatVar);
            
            System.out.println("Bits en el tipo Float: "+ Float.SIZE);
            System.out.println("Bytes en el tipo Float: "+ Float.BYTES);
            System.out.println("Valor mínimo en el tipo Float: "+ Float.MIN_VALUE);
            System.out.println("Valor máximo en el tipo Float: "+ Float.MAX_VALUE);
            System.out.println("\n");
            
            double doubleVar=100.10d;
            System.out.println("doubleVar = " + doubleVar);
            
            System.out.println("Bits en el tipo Double: "+ Double.SIZE);
            System.out.println("Bytes en el tipo Double: "+ Double.BYTES);
            System.out.println("Valor mínimo en el tipo Double: "+ Double.MIN_VALUE);
            System.out.println("Valor máximo en el tipo Double: "+ Double.MAX_VALUE);
           
            
            
    }
    
   
}
